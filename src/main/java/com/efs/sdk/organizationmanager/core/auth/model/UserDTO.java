package com.efs.sdk.organizationmanager.core.auth.model;

import io.swagger.v3.oas.annotations.media.Schema;

import java.util.Objects;

public class UserDTO {
    @Schema(description = "timestamp of user-creation.")
    private Long createdTimestamp;
    @Schema(description = "the user's email.")
    private String email;
    @Schema(description = "whether user is enabled.")
    private boolean enabled;
    @Schema(description = "the user's first name.")
    private String firstName;
    @Schema(description = "keycloak-id of the user.")
    private String id;
    @Schema(description = "the user's last name.")
    private String lastName;
    @Schema(description = "username.")
    private String username;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Long getCreatedTimestamp() {
        return createdTimestamp;
    }

    public void setCreatedTimestamp(Long createdTimestamp) {
        this.createdTimestamp = createdTimestamp;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        UserDTO userDTO = (UserDTO) o;
        return Objects.equals(id, userDTO.id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }
}
